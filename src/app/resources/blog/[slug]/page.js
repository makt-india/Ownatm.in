// ---------------------------
// DYNAMIC METADATA (ENHANCED SEO)
// ---------------------------
export async function generateMetadata({ params }) {
  const { slug } = await params; // Ensure params is awaited
  const post = BLOG_POSTS.find((p) => p.slug === slug);

  if (!post) {
    return {
      title: "Post Not Found – OwnATM Blog",
      description: "This article could not be found on OwnATM Blog.",
      robots: {
        index: false,
        follow: true,
      },
    };
  }

  const url = `https://ownatm.in/resources/blog/${post.slug}`;
  const ogImage = `https://ownatm.in/img/blog/${post.slug}-og.webp`;

  return {
    title: {
      absolute: `${post.title} | OwnATM Blog`, // Using absolute title
    },
    description: post.excerpt,
    keywords: post.keywords || [],

    openGraph: {
      type: "article",
      locale: "en_IN",
      url: url,
      siteName: "OwnATM Blog India",
      title: post.title,
      description: post.excerpt,
      publishedTime: post.date,
      modifiedTime: post.date,
      authors: [post.author],
      section: post.category,
      tags: post.keywords || [],
      images: [
        {
          url: ogImage,
          width: 1200,
          height: 630,
          alt: post.title,
        },
      ],
    },

    twitter: {
      card: "summary_large_image",
      site: "@OwnATMIndia",
      creator: "@OwnATMIndia",
      title: post.title,
      description: post.excerpt,
      images: [ogImage],
    },

    alternates: {
      canonical: url,
    },
  };
}

